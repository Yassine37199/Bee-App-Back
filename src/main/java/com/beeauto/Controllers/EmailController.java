    @Async
    @PostMapping("/send")
    public String send(@RequestBody EmailRequest emailRequest) throws AddressException, MessagingException, IOException {
        Properties props = new Properties();
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.starttls.enable", "true");
        props.put("mail.smtp.host", "smtp.gmail.com");
        props.put("mail.smtp.port", "587");

        Session session = Session.getInstance(props, new javax.mail.Authenticator() {
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(emailRequest.getFrom() , emailRequest.getFromPassword());
            }
        });

        Message msg = new MimeMessage(session);
        msg.setFrom(new InternetAddress(emailRequest.getFrom() , false));
        msg.setRecipients(Message.RecipientType.TO , InternetAddress.parse(emailRequest.getTo()));
        msg.setSubject(emailRequest.getSubject());
        msg.setContent(buildEmail(emailRequest.getContent()) , "text/html");

        Transport.send(msg);
        return "Email send Successfully";

    }


